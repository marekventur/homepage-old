	<h2 class="shadow">Scalable Icons - The @font-face way</h2>

	<p>
		This is the great <a href="http://raphaeljs.com/icons/">Raphaël Icon-Set</a> by Dmitry Baranovskiy
		converted to a <a href="http://www.fontsquirrel.com/fontface/generator">Webfont</a>. Though normaly used as SVGs with the 
		Javascript-Library <a href="http://raphaeljs.com/">Raphaël</a> you can now include them with 
		pure CSS and without the need to have javascript enabled.  
	</p>
	
	<p>	
		 And because the icons are just text you can do all the fancy CSS3 stuff with them. Make sure to try zooming in and out!
	</p>
	
